package operations;

public class Arithmetic_operation {
    public static void main(String[]args){
        byte num1=67;
        byte num2=75;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2);
    }
}
